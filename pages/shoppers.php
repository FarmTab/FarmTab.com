	<h1>Why use Farm Tab?</h1>
	<ul>
	<li>Participate in CSA with the flexibility of a farmers market</li>
	<li>Enables farmers to plan for the season, purchase seed, make repairs, and continue producing the goods you love</li>
	<li>Receive credit bonuses from your farmer as a way of saying "thank you"</li>
	<li>Advocate local sustainable agriculture through linking your facebook or twitter accounts</li>
	<li>Not worry about having to have enough cash to pay for your groceries</li>
	<li>Develop a relationship with the farmer who provides the food you eat</li>
	</ul>
	
	<h1>How do I get started?</h1>
	<p>Go to your local farmers market and tell your farmer you want to sign up for Farm Tab. If they give you a blank stair, you might recommend that they check out farmtab.com and see if they'd like to start their own Farm Tab CSA. If your farmer is setup, they will ask you to fill out a form on their smartphone with your name, email address, and a PIN. That's it!</p>
	
	<h1>How do I add credit to my account?</h1>
	<p>Simply pay your farmer who you are and the amount you want to add. You will be emailed a receipt and your credit is available immediately!</p>
	
	<h1>How do I use credit?</h1>
	<p>When  you are paying your farmer tell them who you are and that you want to pay with Farm Tab. They will bring up your account and subtract the amount you owe for the goods you are purchasing. Before the transaction can be completed, your farmer will ask you to enter your four digit PIN to confirm.</p>
	
	<h1>How do I monitor my transactions?</h1>
	<p>You can get a record of all your transactions simply by signing in with your account here!</p>
	
	<h1>Is there a minimum investment?</h1>
	<p>The minimum investment varies from farm to farm. Ask them for details or check their farm page.</p>
	
	<h1>Does my Farm Tab expire?</h1>
	<p>No, your Farm Tab never expires.</p>
	
	<h1>What is an CSA?</h1>
	<p>CSA allows customers like you and I to pay in advance of harvest. This advanced payment system entitles the customer to goods from the upcoming harvest, which is distributed to CSA members on a weekly basis throughout the year.  A market CSA is more flexible than traditional CSA. Instead of your CSA giving you their selection of goods, you choose exactly what you want from specific farmers throughout the season. The market CSA is preferable option for those who don't need as many goods or as often as many CSA members. </p>
					
					
				 
